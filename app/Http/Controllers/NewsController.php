     * Display the specified resource.
     *
     * @param  \App\News  $news
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $news=News::where('slug','=',$slug)->first();
        $aboutus=AboutUs::all()->first();
        $contactus=ContactUs::orderBy('type','ASC')->get();
        $socialmedia=SocialMedia::all();
        $other_news=News::where('id','<>',$news->id)->orderBy('id','ASC')->limit(10)->get();
        return  view('front.page.news',compact('news','other_news','aboutus','contactus','socialmedia'));
    }

